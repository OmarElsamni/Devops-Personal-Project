kubectl rollout restart deployment/aws-load-balancer-controller -n kube-system

print_info "Waiting for controller to restart..."
kubectl rollout status deployment/aws-load-balancer-controller -n kube-system --timeout=2m

print_info "Deleting and recreating frontend service..."
kubectl delete svc frontend-service -n task-manager 2>/dev/null || true
