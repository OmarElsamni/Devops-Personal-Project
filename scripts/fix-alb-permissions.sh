    --policy-name AWSLoadBalancerControllerIAMPolicy-Enhanced \
    --policy-document file:///tmp/alb-controller-policy.json \
    --query 'Policy.Arn' \
    --output text)

print_info "Created policy: $POLICY_ARN"

# Attach to role
print_info "Attaching policy to role: $ROLE_NAME"
aws iam attach-role-policy \
    --role-name $ROLE_NAME \
    --policy-arn $POLICY_ARN

# Also attach the original policy
